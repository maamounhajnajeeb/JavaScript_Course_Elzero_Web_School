/*
JavaScript Data Types
*/

// string
console.log("Maamoun Haj Najeeb");

console.log(typeof("Maamoun Haj Najeeb"));

console.log(typeof "Maamoun Haj Najeeb");


// number
console.log(500);

console.log(typeof 500);


// number
console.log(500.99);

console.log(typeof 500.99);


// Array => Object
console.log([10, 5, 7, 9]);

console.log(typeof([10, 5, 7, 9]));


// Array => Object
console.log(["Ahmad", "Maamoun", "Ali"]);

console.log(typeof ["Ahmad", "Maamoun", "Ali"]);


// Object
console.log({name: "Maamoun", age:23});

console.log(typeof {name: "Maamoun", age:23});


// Boolean Values
console.log(false);

console.log(typeof false);


// undefined
console.log(undefined);

console.log(typeof undefined);


// null
console.log(null);

console.log(typeof null);
